    /// <summary>
    /// Represents the directions in which a MetroToolBar can expand. Values are Up, Down and Auto.
    /// </summary>
    public enum MetroToolBarExpandDirection
    {
        /// <summary>
        /// The toolbar will expand in the direction with the most room.
        /// </summary>
        Auto,
        /// <summary>
        /// The toolbar will expand upwards.
        /// </summary>
        Up,
        /// <summary>
        /// The toolbar will expand downwards.
        /// </summary>
        Down,
        /// <summary>
        /// The toolbar will not expand. The expand button will be hidden.
        /// </summary>
        None
    }

